class Utils {
    static escapeHtml(unsafe: string | undefined): string {
        if (!unsafe) {
            return ''
        }

        return unsafe
            .replace(/&/g, '&amp;')
            .replace(/</g, '&lt;')
            .replace(/>/g, '&gt;')
            .replace(/"/g, '&quot;')
            .replace(/'/g, '&#039;')
    }
}

export default Utils
